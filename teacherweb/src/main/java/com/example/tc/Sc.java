package com.example.tc;

public class Sc {
    String snumber;
    String cno;

    public String getSnumber() {
        return snumber;
    }

    public void setSnumber(String snumber) {
        this.snumber = snumber;
    }

    public String getCno() {
        return cno;
    }

    public void setCno(String cno) {
        this.cno = cno;
    }

    public Sc(String snumber, String cno) {
        this.snumber = snumber;
        this.cno = cno;
    }
}
