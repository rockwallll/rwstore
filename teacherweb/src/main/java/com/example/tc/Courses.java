package com.example.tc;

public class Courses {
    String cn;
    String cna;

    public String getCn() {
        return cn;
    }

    public void setCn(String cn) {
        this.cn = cn;
    }

    public String getCna() {
        return cna;
    }

    public void setCna(String cna) {
        this.cna = cna;
    }

    public Courses(String cn, String cna) {
        this.cn = cn;
        this.cna = cna;
    }
}
